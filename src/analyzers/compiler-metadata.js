function changed(currentValue, proposedValue) {
  return currentValue !== undefined && proposedValue !== undefined && currentValue !== proposedValue;
}

export function analyzeCompilerMetadata(current, proposed) {
  const findings = [];
  const currentImplementation = current.implementation ?? {};
  const proposedImplementation = proposed.implementation ?? {};
  const currentCompiler = currentImplementation.compiler ?? {};
  const proposedCompiler = proposedImplementation.compiler ?? {};
  const currentMetadata = currentImplementation.metadata ?? {};
  const proposedMetadata = proposedImplementation.metadata ?? {};

  if (changed(currentCompiler.version, proposedCompiler.version)) {
    findings.push({
      id: "COMPILER-001",
      category: "compiler",
      severity: "low",
      title: "The compiler version changed between the two builds",
      body: "Compiler upgrades can be good, but they also widen the review surface because bytecode generation and optimizer behavior may change.",
      evidence: [
        `Current compiler: ${currentCompiler.version}`,
        `Proposed compiler: ${proposedCompiler.version}`
      ],
      recommendation: "Pin the compiler version in CI and verify that the bytecode diff is expected for this upgrade.",
      tags: ["compiler"]
    });
  }

  if (
    changed(currentMetadata.viaIR, proposedMetadata.viaIR) ||
    changed(currentMetadata.evmVersion, proposedMetadata.evmVersion)
  ) {
    findings.push({
      id: "COMPILER-002",
      category: "compiler",
      severity: "medium",
      title: "The code generation strategy or target EVM changed",
      body: "Changes to viaIR or the target EVM version can materially affect generated bytecode and should be reviewed like meaningful implementation changes.",
      evidence: [
        `Current viaIR: ${String(currentMetadata.viaIR)}`,
        `Proposed viaIR: ${String(proposedMetadata.viaIR)}`,
        `Current evmVersion: ${currentMetadata.evmVersion ?? "unset"}`,
        `Proposed evmVersion: ${proposedMetadata.evmVersion ?? "unset"}`
      ],
      recommendation: "Treat this as a code generation change and compare bytecode, gas profile, and regression tests before approving.",
      tags: ["compiler"]
    });
  }

  if (
    changed(currentMetadata.optimizerEnabled, proposedMetadata.optimizerEnabled) ||
    changed(currentMetadata.optimizerRuns, proposedMetadata.optimizerRuns)
  ) {
    findings.push({
      id: "COMPILER-003",
      category: "compiler",
      severity: "low",
      title: "Optimizer settings changed between builds",
      body: "Optimizer setting changes are often intentional, but they can alter control flow and storage access patterns enough to deserve explicit reviewer attention.",
      evidence: [
        `Current optimizer: enabled=${String(currentMetadata.optimizerEnabled)}, runs=${currentMetadata.optimizerRuns ?? "unset"}`,
        `Proposed optimizer: enabled=${String(proposedMetadata.optimizerEnabled)}, runs=${proposedMetadata.optimizerRuns ?? "unset"}`
      ],
      recommendation: "Rebuild both versions with pinned settings and inspect the bytecode and gas deltas before approval.",
      tags: ["compiler"]
    });
  }

  const currentSize = currentImplementation.bytecode?.deployedSize;
  const proposedSize = proposedImplementation.bytecode?.deployedSize;
  if (typeof currentSize === "number" && typeof proposedSize === "number" && Math.abs(proposedSize - currentSize) >= 32) {
    findings.push({
      id: "COMPILER-004",
      category: "compiler",
      severity: "low",
      title: "The deployed bytecode size changed noticeably",
      body: "A meaningful bytecode size jump can be a sign of new logic branches, new dependencies, or different compiler settings.",
      evidence: [
        `Current deployed bytecode size: ${currentSize} bytes`,
        `Proposed deployed bytecode size: ${proposedSize} bytes`
      ],
      recommendation: "Correlate the size delta with the ABI and storage changes to make sure the added code is expected.",
      tags: ["compiler"]
    });
  }

  return findings;
}

